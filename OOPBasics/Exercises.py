# List methods

# index()

spam = ["hi", "how do you do?", 3, 0, "My oh, my...", 0.665, 3]

print(spam.index(3)) # >> 2 (only first entry comes up)

# append()

spam.append("booh!")

print(spam)

# insert()

spam.insert(1, 'dog')

print(spam)

# remove()

spam.remove(3) # only the first entry is removed..

print(spam)
